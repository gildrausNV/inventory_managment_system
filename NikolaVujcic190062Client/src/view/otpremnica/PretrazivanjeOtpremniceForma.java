    private void srediFormu() {
        try {
            Request r = new Request();
            r.setOperation(Operations.VRATI_OTPREMNICE);
            ArrayList<Otpremnica> listaOtpremnica = ControllerClient.getInstance().getOtpremnice(r);
            System.out.println(listaOtpremnica.size());
            ModelTabeleOtpremnice mto = new ModelTabeleOtpremnice(listaOtpremnica);
            tblOtpremnice.setModel(mto);
            JOptionPane.showMessageDialog(this, "Ucitana je lista otpremnica");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Greska pri ucitavanju podataka");
            //Logger.getLogger(PretrazivanjeOtpremniceForma.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
