    private void btnPrikaziActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPrikaziActionPerformed
        try {
            if(txtOdrediste.getText().equals("")){
                JOptionPane.showMessageDialog(this, "Unesite odrediste");
            }
            else{
                String kriterijum = txtOdrediste.getText();
                ModelTabeleOtpremnice mtp = (ModelTabeleOtpremnice) tblOtpremnice.getModel();
                Request r = new Request();
                r.setOperation(Operations.PRETRAZI_OTPREMNICE);
                r.setArgument(kriterijum);
                ArrayList<Otpremnica> lista = ControllerClient.getInstance().getOtpremnice(r);
                mtp.setLista(lista);
                if(lista.size()==0){
                    JOptionPane.showMessageDialog(this, "Sistem ne moze da nadje otpremnice po zadatoj vrednosti");
                }
            }
            
            
        } catch (Exception ex) {
            Logger.getLogger(PretrazivanjeOtpremniceForma.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnPrikaziActionPerformed

