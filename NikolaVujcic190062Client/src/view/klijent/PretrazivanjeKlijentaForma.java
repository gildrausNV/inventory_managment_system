    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(tblKlijenti.getSelectedRow()!=-1){
            try {
                ModelTabeleKlijenti mtk = (ModelTabeleKlijenti) tblKlijenti.getModel();
                Request r = new Request();
                r.setOperation(Operations.OBRISI_KLIJENTA);
                r.setArgument(mtk.getKlijent(tblKlijenti.getSelectedRow()));
                ControllerClient.getInstance().deleteKlijent(r);
                mtk.izbrisi(tblKlijenti.getSelectedRow());
                JOptionPane.showMessageDialog(this, "Klijent je izbrisan");
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Greska");
                //Logger.getLogger(PretrazivanjeKlijentaForma.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void btnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniActionPerformed
        System.out.println(tblKlijenti.getSelectedRow());
        if(tblKlijenti.getSelectedRow()!=-1){
            ModelTabeleKlijenti mtk = (ModelTabeleKlijenti) tblKlijenti.getModel();
            Object klijent = mtk.getKlijent(tblKlijenti.getSelectedRow());
            if(klijent instanceof FizickoLice){
                IzmenaFizickoLice ifl = new IzmenaFizickoLice(null, true, klijent);
                ifl.setVisible(true);
            }
            else{
                IzmenaPravnoLice ipl = new IzmenaPravnoLice(null, true, klijent);
                ipl.setVisible(true);
            }
        }
        else{
            JOptionPane.showMessageDialog(this, "Izaberite klijenta");
        }
    }//GEN-LAST:event_btnIzmeniActionPerformed

    private void btnPrikaziActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPrikaziActionPerformed
        try {
            if(txtIme.getText().equals("")){
                JOptionPane.showMessageDialog(this, "Popunite sva polja");
            }
            else{
                String kriterijum = txtIme.getText();
                ArrayList<Object> lista1 = ControllerClient.getInstance().searchFizickaLica(kriterijum);
                ArrayList<Object> lista2 = ControllerClient.getInstance().searchPravnaLica(kriterijum);
                for (Object o : lista2) {
                    lista1.add(o);
                }
                ModelTabeleKlijenti mtk = (ModelTabeleKlijenti) tblKlijenti.getModel();
                mtk.setLista(lista1);
                if(lista1.size()==0){
                    JOptionPane.showMessageDialog(this, "Sistem ne moze da nadje klijente po zadatoj vrednosti");
                }
            }
            
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Greska");
            //Logger.getLogger(PretrazivanjeKlijentaForma.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnPrikaziActionPerformed

