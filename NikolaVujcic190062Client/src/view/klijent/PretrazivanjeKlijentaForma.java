    private void srediFormu() {
        try {
            ArrayList<FizickoLice> fLica = ControllerClient.getInstance().getFizickaLica();
            ArrayList<PravnoLice> pLica = ControllerClient.getInstance().getPravnaLica();
            ArrayList<Object> lista =  new ArrayList<>();
            for (FizickoLice fl : fLica) {
                lista.add(fl);
            }
            for (PravnoLice pl : pLica) {
                lista.add(pl);
            }
            ModelTabeleKlijenti mtk = new ModelTabeleKlijenti(lista);
            tblKlijenti.setModel(mtk);
            JOptionPane.showMessageDialog(this, "Ucitana je lista klijenata");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Greska pri ucitavanju podataka");
            //Logger.getLogger(PretrazivanjeKlijentaForma.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
