    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            if(cmbTipKlijenta.getSelectedItem() != null && cmbTipKlijenta.getSelectedItem().equals("Fizicko lice")){
                if(txtJMBG.getText().equals("") || txtIme.getText().equals("") || txtPrezime.getText().equals("")) {
                    JOptionPane.showMessageDialog(this, "Popunite sva polja");
                }
                else if(!proveraFL()){
                    JOptionPane.showMessageDialog(this, "Greska pri unosu podataka");
                }
                else{
                    FizickoLice fl=new FizickoLice();
                    fl.setIme(txtIme.getText());
                    fl.setPrezime(txtPrezime.getText());
                    fl.setJMBG(Integer.parseInt(txtJMBG.getText()));
                    ControllerClient.getInstance().createFizickoLice(fl);
                    JOptionPane.showMessageDialog(this, "Klijent je sacuvan");
                }
                
            }
            else{
                if(txtPIB.getText().equals("") || txtNAziv.getText().equals("")) {
                    JOptionPane.showMessageDialog(this, "Popunite sva polja");
                }
                else if(!proveraPL()){
                    JOptionPane.showMessageDialog(this, "Greska pri unosu podataka");
                }
                else{
                    PravnoLice pl=new PravnoLice();
                    pl.setNaziv(txtNAziv.getText());
                    pl.setPIB(Integer.parseInt(txtPIB.getText()));
                    ControllerClient.getInstance().createPravnoLice(pl);
                    JOptionPane.showMessageDialog(this, "Sistem je zapamtio klijenta");
                }
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Sistem ne moze da zapamti ");
            //Logger.getLogger(KreiranjeKlijentaForma.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

