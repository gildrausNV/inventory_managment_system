    private void popuniCmb() {
        cmbTipKlijenta.removeAllItems();
        cmbTipKlijenta.addItem("Fizicko lice");
        cmbTipKlijenta.addItem("Pravno lice");
    }

    private boolean proveraFL() {
        char[] JMBG = txtJMBG.getText().toCharArray();
        char[] ime = txtIme.getText().toCharArray();
        char[] prezime = txtPrezime.getText().toCharArray();
        for (char c : JMBG) {
            if(Character.isLetter(c)) return false;
        }
        for (char c : ime) {
            if(!Character.isLetter(c)) return false;
        }
        for (char c : prezime) {
            if(!Character.isLetter(c)) return false;
        }
        return true;
    }

    private boolean proveraPL() {
        char[] PIB = txtPIB.getText().toCharArray();
        char[] naziv = txtNAziv.getText().toCharArray();
        for (char c : PIB) {
            if(Character.isLetter(c)) return false;
        }
        for (char c : naziv) {
            if(!Character.isLetter(c)) return false;
        }
        return true;
    }
